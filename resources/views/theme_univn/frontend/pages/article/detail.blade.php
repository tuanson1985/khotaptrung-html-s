                            </div>
                            <div class="entry-content">
                                <p>Trước diễn biến phức tạp của dịch bệnh COVID-19, nhiều người dân cả nước đang gặp rất nhiều khó khăn. Thất nghiệp do covid, không có nguồn thu nhập, khiến cho nhiều người phải chạy ăn từng bữa, thậm chí là thiếu ăn.</p>
                                <p>Trước tình hình đó, đã có hàng trăm tổ chức từ thiện, bếp ăn thiện nguyện, những chuyến xe tình nguyện cung cấp lương thực, thực phẩm xuất hiện để hỗ trợ cho những người khó khăn.</p>
                                <p>Tuy nhiên, số người cần hỗ trợ vẫn còn rất nhiều và công tác hỗ trợ cũng gặp nhiều khó khăn. Hiểu được sự khó khăn của người dân trong mùa dịch này, <strong>
                                        <a href="https://sosmap.net/">SOSmap</a>
                                    </strong> – Bản đồ cứu trợ đã ra đời nhằm kết nối thông tin giữa những người cần được hỗ trợ và những người muốn chia sẻ. </p>
                                <p>SOSmap.net do Công ty TNHH công nghệ XTEK với sự hỗ trợ của Hội Liên Hiệp Phụ Nữ Việt Nam, Viện Khoa Học Nông Nghiệp &amp; Phát triển Nông thôn Việt Nam đã sáng tạo ra ứng dụng này từ chương trình “Yêu thương mùa dịch 2021”.</p>
                                <p>
                                    <img decoding="async" class="aligncenter size-full wp-image-1657" src="https://univn.vn/wp-content/uploads/2021/08/sosmap2.png" alt="" width="520" height="369" srcset="https://univn.vn/wp-content/uploads/2021/08/sosmap2.png 520w, https://univn.vn/wp-content/uploads/2021/08/sosmap2-300x213.png 300w, https://univn.vn/wp-content/uploads/2021/08/sosmap2-370x263.png 370w" sizes="(max-width: 520px) 100vw, 520px" />
                                </p>
                                <p>SOSmap sẽ giúp giải quyết những bất cập của hoạt động cứu trợ. Người gặp khó khăn có thể dễ dàng mô tả được chính xác vị trí và nhu cầu của mình.</p>
                                <p>Nhờ đó, các nhà hảo tâm, nhà tài trợ cũng thuận tiện trong việc đóng góp, ủng hộ nguồn lực mà mình đang có đúng với nhu cầu của người cần giúp đỡ.</p>
                                <p>Một trong những tính năng vượt trội của SOSmap chính là việc chia sẻ thông tin để tất cả mọi người dân, tổ chức thiện nguyện, các nhà hảo tâm có thể tham gia quá trình cứu trợ.</p>
                                <p>Sau thời gian chạy thử, SOSmap đã bổ sung tính năng “Giúp đỡ người khó khăn xung quanh bạn” nhằm tận dụng mọi nguồn lực, hỗ trợ nhanh chóng những hoàn cảnh khó khăn ở khoảng cách gần.</p>
                                <p>
                                    <img decoding="async" loading="lazy" class="aligncenter size-full wp-image-1656" src="https://univn.vn/wp-content/uploads/2021/08/sosmap.png" alt="" width="1420" height="728" srcset="https://univn.vn/wp-content/uploads/2021/08/sosmap.png 1420w, https://univn.vn/wp-content/uploads/2021/08/sosmap-300x154.png 300w, https://univn.vn/wp-content/uploads/2021/08/sosmap-1024x525.png 1024w, https://univn.vn/wp-content/uploads/2021/08/sosmap-768x394.png 768w, https://univn.vn/wp-content/uploads/2021/08/sosmap-370x190.png 370w" sizes="(max-width: 1420px) 100vw, 1420px" />
                                </p>
                                <p>SOSmap.net với giao diện đơn giản, dễ dàng sử dụng chỉ cần truy cập sosmap.net và làm theo hướng dẫn.</p>
                                <h2>
                                    <strong>Đối với người cần giúp:</strong>
                                </h2>
                                <p>
                                    <strong>Cách 1:</strong> Gọi vào hotline 19006448 báo cho tổng đài viên xác nhận thông tin mình cần những gì, số địa thoại, địa chỉ. Sau đó tổng đài viên sẽ tự thêm vào hệ thống và tự động được hiển thị lên bản đồ.
                                </p>
                                <p>
                                    <strong>Cách 2:</strong> Truy cập vào địa chỉ https://sosmap.net tìm kiếm các điểm hỗ trợ giống với những thứ mình cần và gọi điện trực tiếp đến đó. Sau đó, đăng ký suất cần nhận. Sau khi đăng ký thành công tổng đài sẽ liên lạc để xác nhận trước khi hiện lên bản đồ. Ngoài ra, ứng dụng sẽ gợi ý những điểm xung quanh phù hợp có thể hỗ trợ người dân và tự người dân cũng có thể gọi điện tới nhờ trợ giúp.
                                </p>
                                <h2>
                                    <strong>Đối với người muốn cho: </strong>
                                </h2>
                                <p>
                                    <strong>Cách 1:</strong> Gọi cho hotline 19006448 báo cho tổng đài viên xác nhận thông tin mình muốn cho là gì, số địa thoại, địa chỉ. Tổng đài viên sẽ tự thêm vào hệ thống và tự động được hiển thị lên bản đồ.
                                </p>
                                <p>
                                    <strong>Cách 2:</strong> Truy cập vào địa chỉ https://sosmap.net đăng ký suất cho. Sau khi đăng ký thành công, tổng đài sẽ liên lạc để xác nhận trước khi hiện lên bản đồ.
                                </p>
                                <p>Chỉ trong vòng gần 10 ngày đầu tiên chương trình được triển khai, SOSmap đã kết nối được hơn 30 mạnh thường quân và đã gởi tặng đến hơn 600 trường hợp đang gặp khó khăn. Hiện nay, danh sách người đăng ký muốn nhận hỗ trợ đã lên tới hơn 2000 trường hợp.</p>
                                <p>SOS ra đời không chỉ dừng lại hỗ trợ trong mùa dịch bệnh, SOS map được kỳ vọng sẽ ứng dụng nhiều hơn trong tương lai. Bản đồ này có thể là nơi kết nối, hỗ trợ nông sản cho bà con nông dân; giúp người dân vùng lũ khắc phục hậu quả của thiên tai; thậm chí có thể là tiếng kêu cứu của những người phụ nữ, những trẻ em bị xâm phạm, bạo hành cần tới sự vào cuộc của xã hội&#8230;.</p>
