    let rateRobux = 6;

    $(function(){
        $("#robux").on("change",function(){
            amountRobux = $(this).val();
            if(amountRobux < 100) {
                Swal.fire({
                    icon: 'error',
                    title: 'Buy Robux',
                    html: 'ขั้นต่ำสั่งซื้อ 100 R$'
                });
                $("#btn-roblox-username").prop('disabled',true);
                return false;
            }
            if(amountRobux > 100000) {
                Swal.fire({
                    icon: 'error',
                    title: 'Buy Robux',
                    html: 'สูงสุดไม่เกิน 100,000 R$'
                });
                $("#btn-roblox-username").prop('disabled',true);
                return false;
            }
            totalAmount = Math.round(amountRobux / rateRobux);
            totalSetPrice = Math.round(((amountRobux * 100) / 70));

            $(".total-set-price").html(number_format(totalSetPrice,0)+' Robux');
            $(".total-robux").html(number_format(amountRobux,0)+' R$');
            $(".total-amount").html(number_format(totalAmount,0));
            $("#btn-roblox-username").prop('disabled',false);
        });

        $(".btn-select-robux").on("click",function(){
            $("#robux").val($(this).data('robux')).trigger('change');
        });

        $("#btn-place-check").on("click",function(){
            btnHtml = $("#btn-place-check").html();
            $("#btn-place-check").html('<i class="fa fa-spin fa-spinner"></i> กำลังตรวจสอบข้อมูลเกม...').prop('disabled',true);

            $.post('/action/shop/buyRobuxVip', {'do':'checkPlaceData','placeId':$("input[name='placeId']").val(),'universeId':$("input[name='universeId']").val()},function(res){
                if(res['result'] == true) {
                    amountRobux = $("#robux").val();
                    totalSetPrice = Math.round(((amountRobux * 100) / 70));

                    $(".total-current-price").html(number_format(res['data']['price'],0)+' Robux');

                    if(totalSetPrice == res['data']['price']) {
                        $("#btn-place-check").hide();
                        $("#btn-vip-buy").show();
                        $("li[data-step=3],li[data-step=4],li[data-step=5]").removeClass('text-white font-weight-bold');
                        $("li[data-step=6]").addClass('text-white font-weight-bold');
                        $("#btn-place-check").html('<i class="fa fa-check"></i> สำเร็จเรียบร้อยแล้ว');
                    } else {
                        $("#btn-place-check").show();
                        $("#btn-vip-buy").hide();
                        $("#btn-place-check").html('<i class="fal fa-times-circle"></i> จำนวน Robux ยังไม่ถูกต้อง');
                    }
                } else {
                    Swal.fire({
                        icon: 'error',
                        title: res['titleMsg'],
                        html: res['msg']
                    });
                }
                $("#btn-place-check").prop('disabled',false);
            },'json');
        });

        $("#form-game_link").on("submit",function(){
            btnHtml = $("#btn-vip-buy").html();
            $("#btn-vip-buy").html('<i class="fa fa-spin fa-spinner"></i> ยืนยันคำสั่งซื้อ...').prop('disabled',true);

            Swal.fire({
                icon: 'info',
                title: 'คำเตือน !',
                html: 'ยอดของ Robux จะเข้าบัญชีท่าน<br>ภายใน <b>5 - 7 วัน</b>หลังจากสั่งซื้อ',
                showCancelButton: true,
                confirmButtonColor: '#3085d6',
                cancelButtonColor: '#d33',
                confirmButtonText: 'ยืนยันการสั่งซื้อ',
                cancelButtonText: 'ยกเลิก',
            }).then((result) => {
                if (result.isConfirmed) {
                    loading('show');

                    $("#btn-vip-buy").html('<i class="fa fa-spin fa-spinner"></i> กำลังดำเนินการสั่งซื้อ...').prop('disabled',true);
                    $.post('/action/shop/buyRobuxVip', {
                        'do' : 'buy',
                        'username' : $("#form-game_link input[name='roblox_username']").val(),
                        'userId' : $("#form-game_link input[name='roblox_userId']").val(),
                        'placeId' : $("#form-game_link input[name='placeId']").val(),
                        'universeId' : $("#form-game_link input[name='universeId']").val(),
                        'robux' : $("#form-game_link input[name='robux']").val(),
                    },function(res){
                        loading('unshow');
                        if(res['result'] == true) {
                            Swal.fire({
                                icon: 'success',
                                title: res['titleMsg'],
                                html: res['msg']
                            }).then((result) => {
                                window.location.href = res['return_url'];
                            });
                        } else {
                            Swal.fire({
                                icon: 'error',
                                title: res['titleMsg'],
                                html: res['msg']
                            }).then((result) => {
                                window.location.reload(true);
                            });
                        }
                    },'json');
                } else {
                    $("#btn-vip-buy").html(btnHtml).prop('disabled',false);
                }
            });

            return false;
        });

        $("#form-roblox_username").on("submit",function(){
            btnHtml = $("#btn-roblox-username").html();
            $("#btn-roblox-username").html('<i class="fa fa-spin fa-spinner"></i> กำลังตรวจสอบ...').prop('disabled',true);

